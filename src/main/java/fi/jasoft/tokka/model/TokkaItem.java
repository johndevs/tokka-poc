package fi.jasoft.tokka.model;

public class TokkaItem {

	private String name;
	
	private boolean done;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public boolean isDone() {
		return done;
	}

	public void setDone(boolean done) {
		this.done = done;
	}
}
